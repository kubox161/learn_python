# L. Для целого числа, если в нем больше трех разрядов, используйте пробел, чтобы сгруппировать разряды по 3.
s=int(input())
print('{:,}'.format(s).replace(',', ' '))

# M. <span>5&nbsp;128&nbsp;Р</span>
# На сайте магазина указана цена за товар в виде такого HTML. &nbsp; дает команду браузеру напечатать неделимый пробел, то есть пробел, по которому не может быть переносов, чтобы цена не сползала по нескольким строкам, но было отделение по разрядам, чтобы удобней было ее читать.
# # Иначе говоря, что у вас просто цена "замусорена"тегами и ненужными элементами.
# Извлеките цену из такой строки и прибавьте к ней 1 (чтобы убедиться, что извлеченное число верное)
s=input()
n=s.replace("&nbsp;", "").replace("<span>", "").replace("Р</span>", "")
print(int(n)+1)

# N. В строке в произвольном месте содержится единственный адрес электронной почты
# (можно опознавать по символу @, он гарантированно встречается только в этом месте).
# Адрес отделен от остальных частей пробелами. Найдите и напечатайте эту почту.
# На вход подается только одна строка, которая может быть очень длинной.
# В примерах ниже она разделена на несколько для удобства чтения условий задачи (иначе она будет слишком длинной и поедет верстка).
s=input()
start=s.index("@")
start_email=s.rindex(" ", 0, start)
end_email=s.index(" ", start)
print(s[start_email+1:end_email])

