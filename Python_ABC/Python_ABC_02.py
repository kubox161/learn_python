# D. В строке записаны два числа, разделенные пробелом. Поменяйте их местами и напечатайте результат.
s = input()
n = s.split()
print(int(n[1]), int(n[0]))

# E. Из почтового адреса нужно извлечь логин (то, что идет до @).
s = input()
