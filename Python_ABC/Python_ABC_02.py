# F. Дан сотовый телефон в формате +71234567890 (11 цифр). Извлеките 3 цифры, являющиеся кодом оператора (которые идут после +7)
s = input()
n = s.index("+7")
print (s[n+2:n+5])

# G. На вход в строке подается ФИО полностью, напечатайте его в сокращенном виде. Обратите внимание на пробелы в ответе.
s=input()
fio=s.split()
print(fio[0], fio[1][0]+"."+fio[2][0]+".")

# H. Напишите сокращатор текстов.
# Необходимо убрать во входной строке части слов -ический, -ическая.
# Разбивать на слова не нужно, просто работайте с подстроками.
s=input()
r=s.replace("ический", ".").replace("ическая", ".")
print(r)

# I. Замените в строке два знака минус на одно длинное тире.
# На вход подается только одна строка, которая может быть очень длинной.
